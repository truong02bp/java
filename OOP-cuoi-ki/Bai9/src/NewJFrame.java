    public void showThuNhap(int ma) {
        double sum = 0;
        DefaultTableModel table = (DefaultTableModel) jTable4.getModel();
        table.setRowCount(0);
        for (DanhSach item : ds) {
            if (item.getCongNhan().getMa() == ma) {
        
                sum+= 450000*item.getCongNhan().getBac()*item.getXuong().getHeSo()*item.getSoNgay()/22;
                table.addRow(new Object[]{item.getXuong().getMa(), item.getXuong().getTen(), item.getXuong().getMoTa(), item.getSoNgay()});
            }

        }
        
        this.sum.setText(String.valueOf(sum));
    }

