    public void showData() {
        c.setRowCount(0);
        x.setRowCount(0);
        d.setRowCount(0);
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        jComboBox6.removeAllItems();
        for (CongNhan item : cn) {
            jComboBox3.addItem(String.valueOf(item.getMa()));
            c.addRow(new Object[]{item.getMa(), item.getTen(), item.getDiaChi(), item.getSdt(), item.getBac()});
        }
        for (Xuong item : xuong) {
            jComboBox4.addItem(String.valueOf(item.getMa()));
            x.addRow(new Object[]{item.getMa(), item.getTen(), item.getMoTa(), item.getHeSo(),});
        }
        boolean[] check = new boolean[1000000];
        for (DanhSach item : ds) {
            int id = item.getCongNhan().getMa();
            if (!check[id]) {
                jComboBox6.addItem(String.valueOf(id));
                check[id] = true;
            }
            d.addRow(new Object[]{item.getCongNhan().getMa(), item.getCongNhan().getTen(), item.getXuong().getMa(), item.getXuong().getTen(), item.getSoNgay()});
        }
    }

