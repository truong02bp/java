    public void addKh(String ten, String diaChi, String loai) {
        String message = KhachHang.check(ten, diaChi);

        try {
            if (!message.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, message);
                throw new HandlerException(message);
            }
            kh.add(new KhachHang(ten, diaChi, loai));
            controller.writeToFile(kh, kPath);
            showData();
        } catch (HandlerException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
