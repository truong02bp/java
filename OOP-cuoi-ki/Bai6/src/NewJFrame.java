    public void showHd(int maKh) {
        DefaultTableModel temp = (DefaultTableModel) jTable5.getModel();
        temp.setRowCount(0);
        int sum = 0;
        for (HoaDon item : hd) {
            if (item.getKhachHang().getMa() == maKh) {
                temp.addRow(new Object[]{item.getDichVu().getMa(), item.getDichVu().getTen(), item.getDichVu().getDonVi(), item.getSl()});
                sum += item.getSl() * item.getDichVu().getGia();
            }
        }
        this.sum.setText(String.valueOf(sum));
    }

    public void addHd(int maKh, int maDv, String sl) {
        String message = HoaDon.check(sl);
        try {
            int index = controller.getIndex(hd, maKh, maDv);
            if (index == -2) {
                message = "Moi khach hang chi dung toi da 5 dich vu";
            }
            if (!message.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, message);
                throw new HandlerException(message);
            }
            if (index == -1) {
                hd.add(new HoaDon(controller.getKh(kh, maKh), controller.getDv(dv, maDv), Integer.parseInt(sl)));
            } else {
                hd.get(index).setSl(hd.get(index).getSl() + Integer.parseInt(sl));
            }
            controller.writeToFile(hd, hPath);
            showData();
        } catch (HandlerException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

