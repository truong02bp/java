    public void showData() {

        k.setRowCount(0);
        d.setRowCount(0);
        h.setRowCount(0);
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        for (KhachHang item : kh) {
            jComboBox3.addItem(String.valueOf(item.getMa()));
            k.addRow(new Object[]{item.getMa(), item.getTen(), item.getDiaChi(), item.getLoai()});
        }
        for (DichVu item : dv) {
            jComboBox4.addItem(String.valueOf(item.getMa()));
            d.addRow(new Object[]{item.getMa(), item.getTen(), item.getDonVi()});
        }
        boolean[] check = new boolean[1000000];
        jComboBox5.removeAllItems();
        for (HoaDon item : hd) {
            h.addRow(new Object[]{item.getKhachHang().getMa(), item.getKhachHang().getTen(), item.getDichVu().getMa(), item.getSl()});
            int id = item.getKhachHang().getMa();
            if (!check[id]) {
                jComboBox5.addItem(String.valueOf(item.getKhachHang().getMa()));
                check[id] = true;
            }
        }
        showHd(hd.get(0).getKhachHang().getMa());
    }

