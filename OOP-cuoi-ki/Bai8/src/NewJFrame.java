    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int maMh = Integer.parseInt((String) jComboBox2.getSelectedItem());
        int maNv = Integer.parseInt((String) jComboBox3.getSelectedItem());
        String sl = jTextField7.getText();
        String message = DanhSach.check(sl);
        try {
            if (!message.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, message);
                throw new HandlerException(message);
            }
            int index = controller.getIndex(ds, maMh, maNv);
            if (index == -1) {
                ds.add(new DanhSach(controller.getMh(mh, maMh), controller.getNv(nv, maNv), Integer.parseInt(sl)));
            } else {
                ds.get(index).setSl(ds.get(index).getSl() + Integer.parseInt(sl));
            }
            controller.writeToFile(ds, dPath);
            showData();
        } catch (HandlerException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

