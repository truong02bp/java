    private void jComboBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox5ActionPerformed
        jComboBox5.addItemListener((e) -> {
            if (e.getStateChange() == ItemEvent.SELECTED){
                showTienCong(Integer.parseInt((String) e.getItem()));
            }
        });
    }//GEN-LAST:event_jComboBox5ActionPerformed
    public void showTienCong(int ma){
        double sum=0;
        DefaultTableModel table = (DefaultTableModel) jTable4.getModel();
        table.setRowCount(0);
        for (DanhSach item : ds){
            if (item.getNhanVienBanHang().getMa() == ma){
                
                sum+= item.getSl()*(item.getMatHang().getGiaBan() - item.getMatHang().getGiaMua());
                table.addRow(new Object[]{item.getMatHang().getMa(),item.getMatHang().getTen(),item.getMatHang().getGiaMua(),item.getMatHang().getGiaBan(),item.getMatHang().getNhom(),item.getSl()});
            }
        }
        this.sum.setText(String.valueOf(sum*2/100));
    }
