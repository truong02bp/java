    public void showTb(int maSv){
        DefaultTableModel table = (DefaultTableModel) jTable4.getModel();
        table.setRowCount(0);
        double sum = 0;
        int tin = 0;
        for (BangDiem item : bd){
            if (item.getSinhVien().getMa() == maSv){
                sum+= item.getDiem()*item.getMonHoc().getTin();
                tin+= item.getMonHoc().getTin();
                table.addRow(new Object[]{item.getMonHoc().getMa(),item.getMonHoc().getTen(),item.getMonHoc().getTin(),item.getMonHoc().getLoai(),item.getDiem()});
            }
        }
        this.sum.setText(String.valueOf(sum/tin));
    }
