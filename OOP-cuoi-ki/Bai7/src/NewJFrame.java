    public void showData() {
        s.setRowCount(0);
        m.setRowCount(0);
        b.setRowCount(0);
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        jComboBox6.removeAllItems();
        for (SinhVien item : sv) {
            jComboBox3.addItem(String.valueOf(item.getMa()));
            s.addRow(new Object[]{item.getMa(), item.getTen(), item.getDiaChi(), item.getNgaySinh(), item.getLop()});
        }
        for (MonHoc item : mh) {
            jComboBox4.addItem(String.valueOf(item.getMa()));
            m.addRow(new Object[]{item.getMa(), item.getTen(), item.getTin(), item.getLoai()});
        }
        boolean[] check = new boolean[100000];
        for (BangDiem item : bd) {
            b.addRow(new Object[]{item.getSinhVien().getMa(), item.getSinhVien().getTen(), item.getMonHoc().getMa(), item.getMonHoc().getTen(), item.getDiem()});
            int id = item.getSinhVien().getMa();
            if (!check[id]){
                jComboBox6.addItem(String.valueOf(id));
                check[id]=true;
            }
        }
    }

